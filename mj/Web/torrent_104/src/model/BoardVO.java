package model;

public class BoardVO {
	private int no;
	private String title;
	private String writer;
	private String time_posted;
	private int recommand;
	private int hits;
	private String link;
	private String content;
	private String id;
	private String category;
	
	public BoardVO() {}
	public BoardVO(int no, String title, String writer, String time_posted,
			int recommand, int hits, String link, String content, String id,
			String category) {
		this.no = no;
		this.title = title;
		this.writer = writer;
		this.time_posted = time_posted;
		this.recommand = recommand;
		this.hits = hits;
		this.link = link;
		this.content = content;
		this.id = id;
		this.category = category;
	}
	
	public BoardVO(String title, String writer, int recommand, int hits,
			String link, String content, String id, String category) {
		super();
		this.title = title;
		this.writer = writer;
		this.recommand = recommand;
		this.hits = hits;
		this.link = link;
		this.content = content;
		this.id = id;
		this.category = category;
	}
	
	public BoardVO(String title, String writer, String link, String content,
			String id, String category) {
		super();
		this.title = title;
		this.writer = writer;
		this.link = link;
		this.content = content;
		this.id = id;
		this.category = category;
	}
	public BoardVO(int no, String title, String writer, String time_posted,int recommand, int hits, String category) {
		this.no = no;
		this.title = title;
		this.writer = writer;
		this.time_posted = time_posted;
		this.recommand = recommand;
		this.hits = hits;
		this.category = category;
	}
	public BoardVO(int no, String title, String link, String content, String category) {
		this.no = no;
		this.title = title;
		this.link = link;
		this.content = content;
		this.category = category;
	}
	public BoardVO(int no, String title, String writer, String time_posted, int hits) {
		this.no = no;
		this.title = title;
		this.writer = writer;
		this.time_posted = time_posted;
		this.hits = hits;
	}
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getTime_posted() {
		return time_posted;
	}
	public void setTime_posted(String time_posted) {
		this.time_posted = time_posted;
	}
	public int getRecommand() {
		return recommand;
	}
	public void setRecommand(int recommand) {
		this.recommand = recommand;
	}
	public int getHits() {
		return hits;
	}
	public void setHits(int hits) {
		this.hits = hits;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	@Override
	public String toString() {
		return "BoardVO [no=" + no + ", title=" + title + ", writer=" + writer
				+ ", time_posted=" + time_posted + ", recommand=" + recommand
				+ ", hits=" + hits + ", link=" + link + ", content=" + content
				+ ", id=" + id + ", category=" + category + "]";
	}
	
}
