package org.kosta.ohanza.model;

public class ReplyDTO {
	private int no;
	private String ssn;
	private String content;
	private String replyDate;
	private int like;
	private int hate;
	
	@Override
	public String toString() {
		return "ReplyDTO [no=" + no + ", ssn=" + ssn + ", content=" + content
				+ ", replyDate=" + replyDate + ", like=" + like + ", hate="
				+ hate + "]";
	}

	public ReplyDTO() {
		super();
	}

	public ReplyDTO(int no, String ssn, String content, String replyDate,
			int like, int hate) {
		super();
		this.no = no;
		this.ssn = ssn;
		this.content = content;
		this.replyDate = replyDate;
		this.like = like;
		this.hate = hate;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getSsn() {
		return ssn;
	}

	public void setSsn(String ssn) {
		this.ssn = ssn;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getReplyDate() {
		return replyDate;
	}

	public void setReplyDate(String replyDate) {
		this.replyDate = replyDate;
	}

	public int getLike() {
		return like;
	}

	public void setLike(int like) {
		this.like = like;
	}

	public int getHate() {
		return hate;
	}

	public void setHate(int hate) {
		this.hate = hate;
	}
}
