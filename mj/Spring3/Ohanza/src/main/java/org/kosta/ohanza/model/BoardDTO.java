package org.kosta.ohanza.model;

public class BoardDTO {
	private int no;
	private String title;
	private String content;
	private int hits;
	private String writeDate;
	private String originalFileName;
	private String category;
	private MemberDTO memberDTO;
	public BoardDTO(int no, String title, String content, int hits,
			String writeDate, String originalFileName, String category,
			MemberDTO memberDTO) {
		super();
		this.no = no;
		this.title = title;
		this.content = content;
		this.hits = hits;
		this.writeDate = writeDate;
		this.originalFileName = originalFileName;
		this.category = category;
		this.memberDTO = memberDTO;
	}
	public BoardDTO() {
		super();
		// TODO Auto-generated constructor stub
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getHits() {
		return hits;
	}
	public void setHits(int hits) {
		this.hits = hits;
	}
	public String getWriteDate() {
		return writeDate;
	}
	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}
	public String getOriginalFileName() {
		return originalFileName;
	}
	public void setOriginalFileName(String originalFileName) {
		this.originalFileName = originalFileName;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public MemberDTO getMemberDTO() {
		return memberDTO;
	}
	public void setMemberDTO(MemberDTO memberDTO) {
		this.memberDTO = memberDTO;
	}
	@Override
	public String toString() {
		return "BoardDTO [no=" + no + ", title=" + title + ", content="
				+ content + ", hits=" + hits + ", writeDate=" + writeDate
				+ ", originalFileName=" + originalFileName + ", category="
				+ category + ", memberDTO=" + memberDTO + "]";
	}
}
