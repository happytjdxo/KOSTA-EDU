package org.kosta.ohanza.model;

public class MemberDTO {
	private String ssn;
	private String id;
	private String password;
	private String name;
	private String address;
	private String nickname;
	private String gender;
	private String grade;
	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getnickname() {
		return nickname;
	}
	public void setnickname(String nickname) {
		this.nickname = nickname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public MemberDTO(String ssn, String id, String password, String name,
			String address, String nickname, String gender, String grade) {
		this.ssn = ssn;
		this.id = id;
		this.password = password;
		this.name = name;
		this.address = address;
		this.nickname = nickname;
		this.gender = gender;
		this.grade = grade;
	}
	public MemberDTO() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	public MemberDTO(String ssn) {
		super();
		this.ssn = ssn;
	}
	
	public MemberDTO(String ssn, String id) { // 비밀번호를 찾기위한 인자값 2개
		this.ssn = ssn;
		this.id = id;
	}
	@Override
	public String toString() {
		return "MemberDTO [ssn=" + ssn + ", id=" + id + ", password="
				+ password + ", name=" + name + ", address=" + address
				+ ", nickname=" + nickname + ", gender=" + gender + ", grade="
				+ grade + "]";
	}
	
	
}
