package chapter02;
/*
step1의 문제점 :
Person의 변수에 실질적인 값을 이미 할당함으로써
Test 클래스에 우리는 김준호의 정보만 출력 받을수 밖에 없었다.
해결책 :
Person 클래스의 변수는 그대로 두고 
여기에 실질적인 값을 할당하는 코드는 Test 클래스에서 하자.
*/

class Person {
	// 변수 (Variable) 선언
	String name;
	int age;
	String address;

	// 기능(Method) 정의
	public void getDetails(){
		System.out.println(name + "\t" + age + "\t" + address);
	} //getDetails
} //class