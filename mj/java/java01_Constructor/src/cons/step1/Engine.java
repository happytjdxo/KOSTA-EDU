package cons.step1;

public class Engine {
	int speed;
	int rpm;
	
	public Engine(int speed, int rpm) {
		this.speed = speed;
		this.rpm = rpm;
		
	}

	public int getSpeed() {
		return speed;
	}

	public int getRpm() {
		return rpm;
	}
	
	public String getEngineInfo(){
		return " 속도 : " + speed + " rpm : " + rpm ;
	}
	
}
