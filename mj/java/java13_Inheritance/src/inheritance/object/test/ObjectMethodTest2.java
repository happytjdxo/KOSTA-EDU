package inheritance.object.test;

public class ObjectMethodTest2 {
	public static void main(String[] args) {
		// 1. String class 에서의 equals(), toString()
		String s1 = new String("kosta");
		String s2 = new String("kosta");
		
		
		
		/*
		 * 							Object			|			String
		 * equals() :				주소값을 문자열로 전환	|			데이타(문자열) 비교
		 * toString() :				주소값 비교			|			데이타(문자열) 출력
		 */
		System.out.println(s1.toString());	// kosta
		System.out.println(s1.toString());	// kosta
		System.out.println(s1.equals(s2));	// true
	}
}
