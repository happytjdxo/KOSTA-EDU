class Member2 {
	static int count;

	Member2() {
		count++;
	}
}

public class StaticExamTest33 {
	public static void main(String[] args) {
		
		Member2 mem1 = new Member2();
		Member2 mem2 = new Member2();
		Member2 mem3 = new Member2();
		Member2 mem4 = new Member2();
		
		System.out.println(mem1.count);
		System.out.println(mem2.count);
		System.out.println(mem3.count);
		System.out.println(mem4.count);
	}
}